    <div class="detail-card">
      <h2>Vocabulaire usine (industrie)</h2>

      <p><strong>Jumbo :</strong> grande bobine/rouleau de matière servant de base avant découpe et transformation.</p>
      <p><strong>Bobine :</strong> rouleau de matière première utilisé pour fabriquer un produit.</p>
      <p><strong>Bobine mère :</strong> bobine principale avant opérations (découpe, traitement, etc.).</p>
      <p><strong>Ligne de production :</strong> ensemble de machines et d’étapes permettant de fabriquer un produit.</p>
      <p><strong>Atelier :</strong> zone de l’usine dédiée à une activité (production, conditionnement, etc.).</p>
      <p><strong>Poste de travail :</strong> emplacement où une tâche est réalisée (machine, contrôle, emballage…).</p>
      <p><strong>Opérateur :</strong> personne qui utilise une machine et réalise des opérations de production.</p>
      <p><strong>Ordre de fabrication (OF) :</strong> document/ordre indiquant quoi produire, en quelle quantité et quand.</p>
      <p><strong>Lot :</strong> ensemble de produits fabriqués dans les mêmes conditions sur une période donnée.</p>
      <p><strong>Cadence :</strong> vitesse de production (quantité produite sur un temps donné).</p>
      <p><strong>Réglage machine :</strong> ajustements avant ou pendant production (alignement, paramètres, qualité…).</p>
      <p><strong>Arrêt planifié :</strong> arrêt prévu (maintenance, réglage, nettoyage, intervention programmée).</p>
      <p><strong>Arrêt non planifié :</strong> arrêt imprévu (panne, incident, sécurité).</p>
      <p><strong>Arrêt machine / arrêt ligne :</strong> arrêt d’une machine ou d’une ligne, temporaire ou long.</p>

      <p><strong>Maintenance :</strong> actions pour maintenir ou réparer les équipements.</p>
      <p><strong>Maintenance préventive :</strong> intervention faite avant une panne pour éviter qu’elle arrive.</p>
      <p><strong>Maintenance corrective :</strong> réparation après panne.</p>

      <p><strong>Contrôle qualité :</strong> vérification que le produit respecte des critères (dimension, aspect, performance…).</p>
      <p><strong>Non-conformité :</strong> résultat qui ne respecte pas les critères attendus.</p>
      <p><strong>Rebut :</strong> produit non conforme jeté ou écarté.</p>
      <p><strong>Retouche :</strong> correction/ajustement d’un produit pour le rendre conforme.</p>

      <p><strong>Traçabilité :</strong> capacité à suivre l’historique d’un produit (lot, date, machine, opérateur…).</p>
      <p><strong>Stock :</strong> quantité disponible de matière première ou de produits finis.</p>
      <p><strong>Flux :</strong> circulation des matières/produits dans l’usine (entrée → production → expédition).</p>
      <p><strong>Expédition :</strong> préparation et envoi des produits vers les clients.</p>
      <p><strong>Magasin / magasinage :</strong> zone de stockage + gestion des entrées/sorties.</p>

      <p><strong>HSE / EHS :</strong> Hygiène Sécurité Environnement (règles et prévention des risques).</p>
      <p><strong>ATEX :</strong> zone à risque d’explosion (poussières/gaz) avec règles de sécurité strictes.</p>
    </div>

    <!-- ================== INFORMATIQUE ================== -->
    <div class="detail-card">
      <h2>Vocabulaire informatique (IT / réseau)</h2>

      <p><strong>Applicatif / application :</strong> logiciel utilisé pour réaliser une tâche (ex : RH, achats, sécurité…).</p>
      <p><strong>Serveur :</strong> ordinateur qui fournit un service (fichiers, application, base de données…).</p>
      <p><strong>Serveur applicatif :</strong> serveur qui héberge une application.</p>
      <p><strong>Serveur de base de données :</strong> serveur qui stocke et gère les données.</p>
      <p><strong>Base de données :</strong> endroit structuré où sont stockées les informations.</p>

      <p><strong>Réseau :</strong> ensemble d’équipements connectés qui communiquent entre eux.</p>
      <p><strong>Switch :</strong> boîtier réseau qui relie plusieurs appareils dans une même zone (PC, imprimantes, machines…).</p>
      <p><strong>Routeur :</strong> équipement qui permet de faire communiquer différents réseaux entre eux.</p>
      <p><strong>Firewall (pare-feu) :</strong> système qui filtre les connexions pour protéger le réseau.</p>
      <p><strong>VLAN :</strong> séparation logique du réseau pour isoler des services (ex : bureautique / production).</p>
      <p><strong>Adresse IP :</strong> “numéro” d’un appareil sur le réseau pour pouvoir le joindre.</p>
      <p><strong>DNS :</strong> service qui transforme un nom (ex : intranet) en adresse IP.</p>
      <p><strong>DHCP :</strong> service qui attribue automatiquement une adresse IP aux appareils.</p>
      <p><strong>Port :</strong> point d’entrée d’un service réseau (ex : web, base de données…).</p>
      <p><strong>Ping :</strong> test simple pour vérifier si un appareil répond sur le réseau.</p>

      <p><strong>Poste utilisateur :</strong> ordinateur utilisé par un collaborateur.</p>
      <p><strong>Compte utilisateur :</strong> identité informatique d’un collaborateur (login/mot de passe).</p>
      <p><strong>Droits d’accès :</strong> autorisations accordées à un utilisateur (lecture, écriture, admin…).</p>
      <p><strong>Authentification :</strong> action de prouver son identité (mot de passe, badge, etc.).</p>

      <p><strong>Ticket :</strong> demande envoyée au support informatique (incident ou service).</p>
      <p><strong>Incident :</strong> problème qui empêche ou gêne l’utilisation d’un outil.</p>
      <p><strong>Demande de service :</strong> demande non urgente (ex : créer un compte, installer un logiciel).</p>
      <p><strong>SLA :</strong> délai de prise en charge/résolution attendu selon l’importance.</p>
      <p><strong>Escalade :</strong> transfert d’un ticket à une équipe spécialisée.</p>

      <p><strong>Sauvegarde (backup) :</strong> copie des données pour éviter leur perte.</p>
      <p><strong>Restauration :</strong> récupération de données après perte ou panne.</p>

      <p><strong>Machine virtuelle (VM) :</strong> “ordinateur virtuel” qui tourne sur un serveur.</p>
      <p><strong>Hyperviseur :</strong> logiciel qui gère les machines virtuelles.</p>
      <p><strong>Mise à jour :</strong> installation d’une nouvelle version d’un logiciel/système.</p>
      <p><strong>Patch :</strong> correctif (souvent sécurité) appliqué à un système.</p>
      <p><strong>Déploiement :</strong> mise à disposition d’une application pour les utilisateurs.</p>
      <p><strong>Production :</strong> version réellement utilisée par les utilisateurs.</p>
      <p><strong>Test / recette :</strong> vérification avant de mettre en production.</p>

      <p><strong>Certificat :</strong> fichier de sécurité qui permet de chiffrer et sécuriser les échanges (ex : HTTPS/TLS).</p>
      <p><strong>Chiffrement :</strong> protection des données pour les rendre illisibles sans clé.</p>
    </div>

